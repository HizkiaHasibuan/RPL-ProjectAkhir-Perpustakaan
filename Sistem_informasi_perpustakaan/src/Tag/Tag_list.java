    private void btn_submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_submitMouseClicked
        String tag = getTag();
        int tag_ID;
        //pengecekan kalau isinya none textbox nya dikosongkan
        if(tag.equals("None")){
            tag_ID = 0;
            tag = "";
        }
        else{
            tag_ID = getTagID() + 1;
        }
        if(from == 0){
            switch (tag_num) {
                case 1:
                    daftar_buku.textbox_tag1.setText(tag);
                    daftar_buku.idTag1 = tag_ID;
                    break;
                case 2:
                    daftar_buku.textbox_tag2.setText(tag);
                    daftar_buku.idTag2 = tag_ID;
                    break;
                case 3:
                    daftar_buku.textbox_tag3.setText(tag);
                    daftar_buku.idTag3 = tag_ID;
                    break;
                default:
                    break;
            }
            if(daftar_buku.isTagListOpen){
                daftar_buku.isTagListOpen = false;
            }
        }
        else if(from == 1){
            switch (tag_num) {
                case 1:
                    tag_search.textbox_tag1.setText(tag);
                    tag_search.idTag1 = tag_ID;
                    break;
                case 2:
                    tag_search.textbox_tag2.setText(tag);
                    tag_search.idTag2 = tag_ID;
                    break;
                case 3:
                    tag_search.textbox_tag3.setText(tag);
                    tag_search.idTag3 = tag_ID;
                    break;
                default:
                    break;
            }
            if(tag_search.isTagListOpen){
                tag_search.isTagListOpen = false;
            }
        }
        else if(from == 2){
            switch (tag_num) {
                case 1:
                    daftar_journal.textbox_tag1.setText(tag);
                    daftar_journal.idTag1 = tag_ID;
                    break;
                case 2:
                    daftar_journal.textbox_tag2.setText(tag);
                    daftar_journal.idTag2 = tag_ID;
                    break;
                case 3:
                    daftar_journal.textbox_tag3.setText(tag);
                    daftar_journal.idTag3 = tag_ID;
                    break;
                default:
                    break;
            }
            if(daftar_journal.isTagListOpen){
                daftar_journal.isTagListOpen = false;
            }
        }
        else if(from == 3){
            switch (tag_num) {
                case 1:
                    daftar_majalah.textbox_tag1.setText(tag);
                    daftar_majalah.idTag1 = tag_ID;
                    break;
                case 2:
                    daftar_majalah.textbox_tag2.setText(tag);
                    daftar_majalah.idTag2 = tag_ID;
                    break;
                case 3:
                    daftar_majalah.textbox_tag3.setText(tag);
                    daftar_majalah.idTag3 = tag_ID;
                    break;
                default:
                    break;
            }
            if(daftar_majalah.isTagListOpen){
                daftar_majalah.isTagListOpen = false;
            }
        }
        this.dispose();
    }//GEN-LAST:event_btn_submitMouseClicked

